package Application;

public class Room {
    private int size;
    private int price;
    private int roomNumber;
    private String reservationDate;


    public int getSize() {
        return size;
    }

    public int getPrice() {
        return price;
    }

    public int getRoomNumber() {
        return roomNumber;
    }

    public String getReservationDate() {
        return reservationDate;
    }
}